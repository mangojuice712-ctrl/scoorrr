// ── Вспомогательные ───────────────────────────────────────────────

function isActive(q, answers) {
  if (!q.conditional) return true;
  return q.conditional.showWhenScores.includes(answers[q.conditional.dependsOn]);
}

// ── Расчёт балла одного блока (0–100%) ────────────────────────────

function calcBlockScore(blockId, answers) {
  const block = BLOCKS.find(b => b.id === blockId);
  if (!block) return null;
  let num = 0, den = 0;
  for (const q of block.questions) {
    if (!isActive(q, answers)) continue;
    const a = answers[q.id];
    if (a === undefined || a === null) continue;
    num += a * q.weight;
    den += 4 * q.weight;
  }
  return den === 0 ? null : Math.round((num / den) * 100);
}

// ── Итоговый расчёт ───────────────────────────────────────────────
// Итоговый скор = только блоки решения (scoring: true)
// Процессные блоки (scoring: false) считаются, но в скор не входят

function calcResults(answers) {
  const scores = {};
  let num = 0, den = 0;

  for (const b of BLOCKS) {
    const s = calcBlockScore(b.id, answers);
    scores[b.id] = s;
    if (b.scoring && s !== null) {
      num += s * b.weight;
      den += b.weight;
    }
  }

  const total = den > 0 ? Math.round(num / den) : 0;
  let zone = total >= 66 ? 'green' : total >= 41 ? 'yellow' : 'red';

  // Блокировки: только если красная зона
  const blockers = [];
  if (zone === 'red') {
    for (const b of BLOCKS) {
      if (b.blocking && scores[b.id] !== null && scores[b.id] < 25) {
        blockers.push(b);
        zone = 'yellow';
      }
    }
  }

  // Слабые места — только из блоков решения
  const weak = [], missing = [];
  for (const b of BLOCKS) {
    for (const q of b.questions) {
      if (!isActive(q, answers)) continue;
      if (answers[q.id] === 0 && b.scoring) weak.push({qId:q.id, qText:q.text, blockName:b.name});
      if (answers[q.id] === null) missing.push({qId:q.id, qText:q.text, blockName:b.name});
    }
  }

  return { total, scores, zone, blockers, weak, missing };
}

// ── Агрегация нескольких ответов ──────────────────────────────────

function calcAggregate(responses) {
  const valid = responses.filter(r => !r.gateFailed && r.result);
  if (valid.length === 0) return null;

  const avgScores = {};
  for (const b of BLOCKS) {
    const vals = valid
      .map(r => r.result.scores && r.result.scores[b.id])
      .filter(v => v !== null && v !== undefined);
    avgScores[b.id] = vals.length > 0
      ? Math.round(vals.reduce((a, x) => a + x, 0) / vals.length)
      : null;
  }

  let num = 0, den = 0;
  for (const b of BLOCKS) {
    if (b.scoring && avgScores[b.id] !== null) {
      num += avgScores[b.id] * b.weight;
      den += b.weight;
    }
  }
  const total = den > 0 ? Math.round(num / den) : 0;
  let zone = total >= 66 ? 'green' : total >= 41 ? 'yellow' : 'red';
  const blockers = [];
  if (zone === 'red') {
    for (const b of BLOCKS) {
      if (b.blocking && avgScores[b.id] !== null && avgScores[b.id] < 25) {
        blockers.push(b); zone = 'yellow';
      }
    }
  }
  return { total, scores: avgScores, zone, blockers, count: valid.length };
}

// ── UI-утилиты ────────────────────────────────────────────────────

function scoreColor(s) {
  if (s === null) return '#94a3b8';
  if (s >= 66) return '#16a34a';
  if (s >= 41) return '#d97706';
  return '#dc2626';
}

function escHtml(s) {
  return String(s || '')
    .replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}
